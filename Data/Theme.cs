namespace cocoding.Data;

/// <summary>
/// Enumeration of possible theme settings.
/// </summary>
public enum Theme
{
    /// <summary>
    /// Dark if the system is set to dark, otherwise light.
    /// </summary>
    System = -1,
    
    /// <summary>
    /// Light theme.
    /// </summary>
    Light = 0,
    
    /// <summary>
    /// Dark theme.
    /// </summary>
    Dark = 1
}